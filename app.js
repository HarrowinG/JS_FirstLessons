//value types
console.log(2019, 1.5);
console.log(2019 * 'asd'); //NaN
console.log(1 / 0); //Infinity

console.log('Hello', "Hello", `Hello`);

console.log(true, false);

console.log(null);

console.log(undefined);  //empty variable

console.log(Symbol());

//reference types
console.log({ name: 'Me', age: 30 });
console.log([1, 2, 3, 4]);
